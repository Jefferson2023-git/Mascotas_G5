package com.example.animation2;

public class Perro {

    String nombre;
    double altura;
    double peso;
    String colorPelaje;
    String genero;
    String raza;


    public Perro(String nombre, double altura, double peso, String colorPelaje, String genero, String raza) {
        this.nombre = nombre;
        this.altura = altura;
        this.peso = peso;
        this.colorPelaje = colorPelaje;
        this.genero = genero;
        this.raza = raza;
    }


    }





